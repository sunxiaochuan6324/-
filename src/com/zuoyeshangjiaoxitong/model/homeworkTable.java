package com.zuoyeshangjiaoxitong.model;

public class homeworkTable {
	private int homeworkid;
	private String homeworkname;
	private String homeworkpath;
	private String homeworkdesc;
	
	public int getHomeworkid() {
		return homeworkid;
	}
	public void setHomeworkid(int homeworkid) {
		this.homeworkid = homeworkid;
	}
	public String getHomeworkname() {
		return homeworkname;
	}
	public void setHomeworkname(String homeworkname) {
		this.homeworkname = homeworkname;
	}
	public String getHomeworkpath() {
		return homeworkpath;
	}
	public void setHomeworkpath(String homeworkpath) {
		this.homeworkpath = homeworkpath;
	}
	public String getHomeworkdesc() {
		return homeworkdesc;
	}
	public void setHomeworkdesc(String homeworkdesc) {
		this.homeworkdesc = homeworkdesc;
	}

}
